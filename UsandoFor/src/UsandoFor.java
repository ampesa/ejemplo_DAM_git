
public class UsandoFor {

	public static void main(String[] args) {
		// Estructura de control for
		// inicialización; condicion; paso
		for (int i=0;i<10;i++){
			System.out.println(i);
		}
		for (int i=10;i>0;i--){
			int j=3;
			System.out.println("Variable i:"+i);
			System.out.println("Variable j:"+j);
		}

	}

}
